    public void mostrarDefiniciones(){
            DefaultTableModel datosTabla = new DefaultTableModel();
        
            //selecciono el modelo de datos de la tabla para modificarlo
            datosTabla = (DefaultTableModel) jTable1.getModel();
            datosTabla.setRowCount(termi.getDefiniciones().size());
            
           
            
            //Paso la información a la tabla
            for(int i = 0; i < termi.getDefiniciones().size(); i++){
                datosTabla.setValueAt(termi.getDefiniciones().get(i).getDefinición(),i,0);
                datosTabla.setValueAt(termi.getDefiniciones().get(i).getAutor(),i,1);
                datosTabla.setValueAt(termi.getDefiniciones().get(i).getNombreArticulo() ,i,2);
                datosTabla.setValueAt(termi.getDefiniciones().get(i).getNombreArchivo(),i,3);
                datosTabla.setValueAt(termi.getDefiniciones().get(i).getPagDefinicion(),i,4);
                datosTabla.setValueAt(termi.getDefiniciones().get(i).getLocalizacionPC(),i,5);               
                datosTabla.setValueAt(termi.getDefiniciones().get(i).getDia()+"/"+termi.getDefiniciones().get(i).getMes()+"/"+termi.getDefiniciones().get(i).getAnno(),i,6);
            }
    }
    
    
