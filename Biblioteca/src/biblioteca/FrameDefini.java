    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
             Calendar c = Calendar.getInstance();
             c.setTime(new Date());
        
             String   dia = Integer.toString(c.get(Calendar.DATE));
             int mes = (c.get(Calendar.MONTH));
             String   annio = Integer.toString(c.get(Calendar.YEAR));
             mes++; 
        
        TrabajoXML xml = new TrabajoXML();
        DifinicionAutor nuevaDifi = new DifinicionAutor();
        nuevaDifi.setAnno(annio);
        nuevaDifi.setDia(dia);
        nuevaDifi.setMes(String.valueOf(mes));
        
        nuevaDifi.setDefinición(jTextArea1.getText());
        nuevaDifi.setAutor(jComboBox1.getSelectedItem().toString());
        nuevaDifi.setNombreArchivo(jComboBox2.getSelectedItem().toString());
        nuevaDifi.setNombreArticulo(jTextField4.getText());
        nuevaDifi.setLocalizacionPC(jTextField3.getText());
        nuevaDifi.setPagDefinicion(Integer.parseInt(jTextField1.getText()));
        
        this.termi.getDefiniciones().add(nuevaDifi);
        try {
            xml.annadirDifinicion(termi);
            mostrarDefiniciones();
        } catch (Exception ex) {
            Logger.getLogger(FrameDefini.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

