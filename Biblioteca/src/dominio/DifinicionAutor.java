public class DifinicionAutor {
    private String definición;
    private String autor;
    private int pagDefinicion;
    private String nombreArchivo;
    private String localizacionPC;
    private String nombreArticulo;
    private String dia;
    private String mes;
    private String anno;

    /**
     * @return the definición
     */
    public String getDefinición() {
        return definición;
    }

    /**
     * @param definición the definición to set
     */
    public void setDefinición(String definición) {
        this.definición = definición;
    }

    /**
     * @return the autor
     */
    public String getAutor() {
        return autor;
    }

    /**
     * @param autor the autor to set
     */
    public void setAutor(String autor) {
        this.autor = autor;
    }

    /**
     * @return the pagDefinicion
     */
    public int getPagDefinicion() {
        return pagDefinicion;
    }

    /**
     * @param pagDefinicion the pagDefinicion to set
     */
    public void setPagDefinicion(int pagDefinicion) {
        this.pagDefinicion = pagDefinicion;
    }

    /**
     * @return the nombreArchivo
     */
    public String getNombreArchivo() {
        return nombreArchivo;
    }

    /**
     * @param nombreArchivo the nombreArchivo to set
     */
    public void setNombreArchivo(String nombreArchivo) {
        this.nombreArchivo = nombreArchivo;
    }

    /**
     * @return the localizacionPC
     */
    public String getLocalizacionPC() {
        return localizacionPC;
    }

    /**
     * @param localizacionPC the localizacionPC to set
     */
    public void setLocalizacionPC(String localizacionPC) {
        this.localizacionPC = localizacionPC;
    }

    /**
     * @return the nombreArticulo
     */
    public String getNombreArticulo() {
        return nombreArticulo;
    }

    /**
     * @param nombreArticulo the nombreArticulo to set
     */
    public void setNombreArticulo(String nombreArticulo) {
        this.nombreArticulo = nombreArticulo;
    }

    /**
     * @return the dia
     */
    public String getDia() {
        return dia;
    }

    /**
     * @param dia the dia to set
     */
    public void setDia(String dia) {
        this.dia = dia;
    }

    /**
     * @return the mes
     */
    public String getMes() {
        return mes;
    }

    /**
     * @param mes the mes to set
     */
    public void setMes(String mes) {
        this.mes = mes;
    }

    /**
     * @return the anno
     */
    public String getAnno() {
        return anno;
    }

    /**
     * @param anno the anno to set
     */
    public void setAnno(String anno) {
        this.anno = anno;
    }
    
    
    
}
